package cine;

public class Combos {
	private String gaseosas;
	private String pipoca;
	private int chocolates;
	private int precio;
	public String getGaseosas() {
		return gaseosas;
	}
	public String getPipoca() {
		return pipoca;
	}
	public int getChocolates() {
		return chocolates;
	}
	public int getPrecio() {
		return precio;
	}
	public void setGaseosas(String gaseosas) {
		this.gaseosas = gaseosas;
	}
	public void setPipoca(String pipoca) {
		this.pipoca = pipoca;
	}
	public void setChocolates(int chocolates) {
		this.chocolates = chocolates;
	}
	public void setPrecio(int precio) {
		this.precio = precio;
	}
	
	
}
