package builder;

public class Pizza {
String masa;
	String salsa;
	String tipoQueso;

	public String getMasa() {
	return masa;
}
public void setMasa(String masa) {
	this.masa = masa;
}
public String getSalsa() {
	return salsa;
}
public void setSalsa(String salsa) {
	this.salsa = salsa;
}
public String getTipoQueso() {
	return tipoQueso;
}
public void setTipoQueso(String tipoQueso) {
	this.tipoQueso = tipoQueso;
}

}
